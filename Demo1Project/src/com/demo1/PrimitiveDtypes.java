package com.demo1;

public class PrimitiveDtypes {
	public static void main(String[] args) {
		// 1. small numerical value
		byte experienceYears = 5;
		
		// 2. short - medium range number
		short batchCode = 2354;
		
		// 3. int - whole number
		int jerseyNumber = 18;
		
		// 4. long - large numerical value
		long companyRevenue = 18000000000000L;
		
		// 5. float - decimal with less precision
		float attendencePercentage = 18.6F;
		
		// 6. double - Decimal with high precision
		double salary = 18000.65;
		
		//7. char - Single character
		char grade = 'A';
		
		//8. boolean - True or False
		boolean isPermanent = true;
		
		System.out.println("employees experience :" + experienceYears);
		System.out.println("batchcode :" + batchCode);
		System.out.println("jersey Number :" + jerseyNumber);
		System.out.println("company Revenue :" + companyRevenue);
		System.out.println("attendence percentage :" + attendencePercentage);
		System.out.println("salary :" + salary);
		System.out.println("grade :" + grade);
		System.out.println("boolean value :" + isPermanent);
		
	}
}
